// Helper: expand compact district + tambons spec into full object.
// Tambons given as string array are positioned in a small ring around the district center
// (real coordinates would need full data; for prototype this is plenty accurate at radius scale).
function _district([name, lat, lng, tambonsRaw]) {
  const tambons = (tambonsRaw || []).map((t, i, arr) => {
    if (typeof t === "object") return t;
    // Position tambons in a small ring (~2.5 km) around the district center
    const angle = (i / arr.length) * Math.PI * 2;
    const r = 0.022; // ~2.4 km
    return {
      name: t,
      lat: lat + r * Math.cos(angle),
      lng: lng + r * Math.sin(angle),
    };
  });
  return { name, lat, lng, tambons };
}
