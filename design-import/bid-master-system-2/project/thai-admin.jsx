// Haversine distance in km
function distanceKm(lat1, lng1, lat2, lng2) {
  if ([lat1, lng1, lat2, lng2].some(v => v == null || isNaN(v))) return Infinity;
  const R = 6371;
  const toRad = (d) => d * Math.PI / 180;
  const dLat = toRad(lat2 - lat1);
  const dLng = toRad(lng2 - lng1);
  const a = Math.sin(dLat / 2) ** 2 +
    Math.cos(toRad(lat1)) * Math.cos(toRad(lat2)) *
    Math.sin(dLng / 2) ** 2;
  return 2 * R * Math.asin(Math.sqrt(a));
}

function districtsWithinRadius(gps, radiusKm) {
  if (!gps || !radiusKm || radiusKm <= 0) return [];
  const out = [];
  for (const province of Object.keys(THAI_GEO)) {
    for (const d of THAI_GEO[province]) {
      const dist = distanceKm(gps.lat, gps.lng, d.lat, d.lng);
      if (dist <= radiusKm) {
        out.push({ province, district: d.name, distanceKm: dist });
      }
    }
  }
  return out.sort((a, b) => a.distanceKm - b.distanceKm);
}

function hasGeoData(province) {
  return !!THAI_GEO[province];
}

